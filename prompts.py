def build_descriptive_prompt(word_limit_text: str, problem_text: str) -> str:
    return f"""
    あなたは論理的思考力を問う国語の試験問題を作成する専門家です。出力は必ず日本語で、指定フォーマットのみを出力すること。
    以下の{{文章}}を読み、字数制限は{{字数制限}}として、
    論理的読解力を問う**字数指定の記述式問題**を合計5問作成してください。

    要件：
    ・文体は常体（である調）で統一すること。です・ます調は禁止。
    ・各設問は「{{字数制限}}字以内で答えなさい」とし、模範解答は必ずその字数内に収め、末尾に「（◯◯字）」と実際の文字数を明記すること。
    ・本文の単純な要約にとどまらず、論理展開・対比・因果・含意・前提/結論・反論処理など、筆者の主張や論理構造を問うこと。
    ・設問は読み違いが生じないように、本文表現に根拠を持たせること。
    ・各設問に対して採点基準となるキーワード（箇条書き3〜6項目程度）を示すこと（キーワード自体の語尾は常体でなくてよい）。
    ・出力は指定フォーマット以外の余分なテキストを含めないこと（前置き・後書き・注釈は禁止）。

    出力フォーマット（問題ごとにこの順番で繰り返す。合計5問）：
    【問◯】（設問文）
    【出題意図】（この設問で問いたい論理的思考のポイント）
    【模範解答】（記述解答例。末尾に「（◯◯字）」）
    【採点基準となるキーワード】（箇条書き）

    {{字数制限}} = {word_limit_text}
    {{文章}} = {problem_text}"""


def build_multiple_choice_prompt(num_choices: int, problem_text: str) -> str:
    return f"""
    あなたは論理的思考力を問う国語の試験問題を作成する専門家です。出力は必ず日本語で、指定フォーマットのみを出力すること。
    以下の{{文章}}を読み、
    論理的読解力を問う**多肢択一問題**を合計5問作成してください。

    要件：
    ・文体は常体（である調）で統一すること。です・ます調は禁止。
    ・各設問は選択肢をちょうど{{選択肢数}}個用意し、番号は1〜{{選択肢数}}で表記すること（A/B表記は禁止）。
    ・正解は必ず1つのみ。残りは本文の表現や論理の取り違え、条件の見落としなどのもっともらしい誤答にすること。
    ・設問は本文の内容理解にとどまらず、論理展開・対比・因果・含意・前提/結論・反論処理など、筆者の主張や論理構造を問うこと。
    ・各選択肢には十分な差異を持たせ、重複や曖昧さを避けること。
    ・出力は指定フォーマット以外の余分なテキストを含めないこと（前置き・後書き・注釈は禁止）。

    出力フォーマット（問題ごとにこの順番で繰り返す。合計5問）：
    【問◯】（設問文）
    【選択肢】1. ... / 2. ... / 3. ... / ...（{{選択肢数}}個すべて列挙）
    【出題意図】（この設問で問いたい論理的思考のポイント）
    【模範解答】（正解の番号と、本文に基づく詳細な根拠）
    【不正解】（各不正解選択肢ごとに、誤りの理由を簡潔に説明）

    {{選択肢数}} = {num_choices}
    {{文章}} = {problem_text}"""


